    private void Actualizar(){
        try {
            miConexion = conexion.Connection().getConnection();
            String query ="SELECT * FROM query_movimiento WHERE tipo_mov='Reparacion' AND status=1 GROUP BY id_mov";         
            // create the java statement
            Statement st = miConexion.createStatement();
            ResultSet rs = st.executeQuery(query);
            //checamos que haya usuarios correctos 
            cantpro=0;
            while(rs.next()){
               
                String datos[]={rs.getString("id_mov"),rs.getString("tipo_mov"),rs.getString("fecha_mov"),rs.getString("cantidad_mov"),rs.getString("producto"),rs.getString("usuario"),rs.getString("provedor")};
                table.addRow(datos);
                cantpro+=Integer.parseInt(rs.getString("cantidad_mov"));
            }
           this.cantidad.setText(Integer.toString(cantpro));
        } catch (SQLException ex) {
            Logger.getLogger(Reparacion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
