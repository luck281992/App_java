    private void reparadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reparadoActionPerformed
                   // TODO add your handling code here:
       int cantfila = table.getRowCount();
       int fila = tabla.getSelectedRow();
       if(cantfila==0){
         JOptionPane.showMessageDialog(null,"No hay Mobiliario en repracion","Error reparacion",JOptionPane.ERROR_MESSAGE);
         return;  
       }
      
       if(fila==-1){
         JOptionPane.showMessageDialog(null, "Seleccionar Registro de Mobiliario a Reparar","Error reparacion",JOptionPane.ERROR_MESSAGE);
         return;
       }
       try {  
            int id;         
            miConexion = conexion.Connection().getConnection();
            id = Integer.parseInt(tabla.getValueAt(fila, 0).toString());
            //cambiamos el estatus como reparado o funcional y regresamos existencia a nuestro producto
            // de que ya regreso el mobiliario que estaba en reparacion
            String queryupdate = "UPDATE Movimientos SET status=0 WHERE id_mov='"+id+"' ";
            Statement st = miConexion.createStatement();
            int cant = st.executeUpdate(queryupdate);
            if(cant >0){
                JOptionPane.showMessageDialog(null, "Se Regreso Mobiliario Reparado correctamente!");
                LimpiarJTable();
                Actualizar();
            }else
                JOptionPane.showMessageDialog(null, "Error al devolver Mobiliario reparado Intentelo de nuevo!","Error Reparacion",JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(Reparacion.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            Mysqlconector.liberaConexion(miConexion);
        }
    }//GEN-LAST:event_reparadoActionPerformed

